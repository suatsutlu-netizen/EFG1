const CACHE_NAME = 'essai-frein-v1';
const ASSETS = [
  './',
  './index.html',
  './style.css',
  './app.js',
  './manifest.json'
];

self.addEventListener('install', (e) => {
  e.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      return cache.addAll(ASSETS);
    }).then(() => self.skipWaiting())
  );
});

self.addEventListener('activate', (e) => {
  e.waitUntil(
    caches.keys().then((keys) => {
      return Promise.all(
        keys.map((key) => {
          if (key !== CACHE_NAME) {
            return caches.delete(key);
          }
        })
      );
    }).then(() => self.clients.claim())
  );
});

self.addEventListener('fetch', (e) => {
  e.respondWith(
    caches.match(e.request).then((cachedResponse) => {
      return cachedResponse || fetch(e.request);
    })
  );
});

// Gestion des notifications pour l'alerte en arrière-plan
self.addEventListener('userinstance-trigger-alarm', (e) => {
  self.registration.showNotification("Alerte Essai de Frein", {
    body: "Le temporisateur est écoulé. Action requise !",
    tag: "brake-alert",
    renotify: true,
    requireInteraction: true
  });
});